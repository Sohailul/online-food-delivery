     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $id = Auth::user()->id;
        if(Auth::user()->roles == 'admin'){
            $tickets = Ticket::all()->whereIn('status',['opened']);
            return view('admin.pages.Ticket.opened', compact('tickets'));
        }
        else{
            $tickets = Ticket::where('customer_id', $id)->whereIn('status', ['opened'])->get();
            return view('admin.pages.Ticket.opened', compact('tickets'));
        }
    }

